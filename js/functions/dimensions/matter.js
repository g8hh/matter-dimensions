functions['matter_1_pow'] = (amt_bought, amt) => {
    var base_exp = big(2);
    // p15: multiplier per 1st Matter Dimension becomes x11
    base_exp = player.upgrades["p15"].get_effect();

    var base_pow = base_exp.pow(amt_bought);
    // p12: bonus to 1st Matter Dims
    base_pow = base_pow.mult(player.upgrades["p12"].get_effect());
    // achievement 18: x8 power to matter dimensions
    if (player.achievements['18'].complete) base_pow = base_pow.mult(8);
    // light boosts dimensions
    base_pow = base_pow.mult(power_light_production());
    // p32: bonus to all Matter Dimensions
    base_pow = base_pow.mult(player.upgrades['p32'].get_effect());
    // p34: bonus to all Matter Dimensions
    if (player.upgrades['p34'].is_active()) base_pow = base_pow.mult(player.upgrades['p34'].get_effect());
    // n11: bonus to all Matter Dimensions
    if (player.upgrades['n11'].is_active()) base_pow = base_pow.mult(player.upgrades['n11'].get_effect());
    // n13: bonus to all Matter Dimensions
    if (player.upgrades['n13'].is_active()) base_pow = base_pow.mult(player.upgrades['n13'].get_effect());
    // v11: x100 bonus to all Matter Dimensions
    if (player.upgrades['v11'].is_active()) base_pow = base_pow.mult(100);
    // v111: x1e6 power to all Matter Dimensions
    base_pow = base_pow.mult(player.upgrades['v111'].get_effect());
    // v161: bonus to all Matter Dimensions
    if (player.upgrades['v161'].is_active()) base_pow = base_pow.mult(player.upgrades['v161'].get_effect());
    // v191: bonus to all Matter Dimensions
    if (player.upgrades['v191'].is_active()) base_pow = base_pow.mult(player.upgrades['v191'].get_effect());
    // inflation boosts 1st Matter Dimension
    base_pow = base_pow.mult(power_inflation_matter());
    // red waves boost Matter Dimensions
    base_pow = base_pow.mult(wave_effect('red'));
    // p51: Photonic dimensions boost Matter dimensions
    base_pow = base_pow.mult(player.upgrades['p51'].get_effect().pow(player.dimensions['photonic_1'].amt_bought));
    // Dimensional Dimensions boost respective dimensions
    base_pow = base_pow.mult(power_manifolds().pow(player.dimensions['dimensional_1'].amt_bought));
    // evolution b09: boost all Matter Dimensions based on current matter
    if (player.evolutions['b09'].is_active()) base_pow = base_pow.mult(player.evolutions['b09'].get_secondary_effect());
    // evolution b10: boost all Dimensions based on unspent Genes
    if (player.evolutions['b10'].is_active()) base_pow = base_pow.mult(player.evolutions['b10'].get_secondary_effect());

    // challenge d5: raise Matter Dim multipliers to a power
    if (!player.challenges['d0'].inC() && (player.challenges['d5'].inC() || player.challenges['d5'].completed)) base_pow = base_pow.pow(1.5);

    return base_pow;
};
